     public String formataSituacao(String ent){
    
        if(ent.equals("I"))
           return "Inativo";
        else 
           return "Ativo";
        
    }
     public DefaultTableModel montaTabela(){
      DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Código");
            modelo.addColumn("Descrição");
            modelo.addColumn("Situacão");
     return (modelo);
 }
     public void Exibir(ArrayList<TipoPagamento> tpPagamentos){
        
         DefaultTableModel modelo=montaTabela();
         if(tpPagamentos.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nenhum Tipo de Pagamento encontrado");
        }else{
            for(int i =0; i< tpPagamentos.size(); i++){
                tipoPagamento=daoTpPagamento.consultar((tpPagamentos.get(i).getCodigo()));             
                modelo.addRow(new Object[]{String.valueOf(tipoPagamento.getCodigo()),tipoPagamento.getDescricao(),formataSituacao(tipoPagamento.getSituacao())});
            }
        }
        TabelaTipoPag.setModel(modelo);
        TabelaTipoPag.setDefaultEditor(Object.class, null);

     }

