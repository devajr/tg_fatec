    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        
        if(daoTpPagamento.obterTpPag()<Integer.parseInt(txtCodigo.getText())){
                tipoPagamento=new TipoPagamento(Integer.parseInt(txtCodigo.getText()));
                tipoPagamento.setDescricao(txtDescriçao.getText());
                if(rbAtivo.isSelected()){
                    tipoPagamento.setSituacao("A");
                }
                else if(rbInativo.isSelected()){
                    tipoPagamento.setSituacao("I");
                }

                if (txtDescriçao.getText().length() > 0) {
                    jLabelDescricao.setVisible(false);
                } else {
                    jLabelDescricao.setVisible(true);
                }
                if(txtDescriçao.getText().length()>0 && rbAtivo.isSelected()==true || rbInativo.isSelected()==true){

                    JOptionPane.showMessageDialog(this, "Tipo de Pagamento Cadastrado com Sucesso!");
                    daoTpPagamento.inserir(tipoPagamento);
                    txtDescriçao.setText("");
                    rbAtivo.setSelected(false);
                    rbInativo.setSelected(false);
                    jLabelDescricao.setVisible(false);
                    txtCodigo.setText(String.valueOf(daoTpPagamento.obterTpPag()));
                    listar();
                    
                }
                else{
                    JOptionPane.showMessageDialog(this, "Confira os campos Obrigatórios!!");
                }
        }
        else{
                tipoPagamento=new TipoPagamento(Integer.parseInt(txtCodigo.getText()));
                tipoPagamento.setDescricao(txtDescriçao.getText());
                if(rbAtivo.isSelected()){
                    tipoPagamento.setSituacao("A");
                }
                else if(rbInativo.isSelected()){
                    tipoPagamento.setSituacao("I");
                }

                if (txtDescriçao.getText().length() > 0) {
                    jLabelDescricao.setVisible(false);
                } else {
                    jLabelDescricao.setVisible(true);
                }
                if(txtDescriçao.getText().length()>0 && rbAtivo.isSelected()==true || rbInativo.isSelected()==true){

                    JOptionPane.showMessageDialog(this, "Tipo de Pagamento Alterado com Sucesso!");
                    daoTpPagamento.alterar(tipoPagamento);
                    
                }
                 else{
                   JOptionPane.showMessageDialog(this, "Confira os campos Obrigatórios!!");
                } 

            
        }
         jTabProcurar.setSelectedIndex(0);
         listar();
    }//GEN-LAST:event_btnGravarActionPerformed


