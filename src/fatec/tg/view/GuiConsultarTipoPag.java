    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        if(txtCodigo1.getText().isEmpty()==false && txtDescricao.getText().isEmpty() && cbxSituaçao.getSelectedIndex()==0){
              tipoPagamento=daoTpPagamento.consultar(Integer.parseInt(txtCodigo1.getText())); 
              DefaultTableModel modelo = montaTabela();
              if(tipoPagamento==null){
                JOptionPane.showMessageDialog(this, "Tipo de Pagamento não encontrado");
              }else{         
                modelo.addRow(new Object[]{String.valueOf(tipoPagamento.getCodigo()),tipoPagamento.getDescricao(),formataSituacao(tipoPagamento.getSituacao())});
              }
            TabelaTipoPag.setModel(modelo);
            TabelaTipoPag.setDefaultEditor(Object.class, null);
        }
        else if(txtCodigo1.getText().isEmpty() && txtDescricao.getText().isEmpty()==false && cbxSituaçao.getSelectedIndex()==0){
            ArrayList<TipoPagamento> tpPagamentos=daoTpPagamento.listarTpPagamentoDescricao(txtDescricao.getText());
            Exibir(tpPagamentos);
        }
        else if(txtCodigo1.getText().isEmpty()&& txtDescricao.getText().isEmpty() && cbxSituaçao.getSelectedItem()!="Selecione"){
            ArrayList<TipoPagamento> tpPagamentos=daoTpPagamento.listarTpPagSituacao((String)cbxSituaçao.getSelectedItem());
            Exibir(tpPagamentos);
        }
        else if(txtCodigo1.getText().isEmpty() && txtDescricao.getText().isEmpty() && cbxSituaçao.getSelectedIndex()==0)
        JOptionPane.showMessageDialog(this, "Preencha um campo de filtro");
        else{
             JOptionPane.showMessageDialog(this, "Apenas um campo de filtro deve ser preenchido");
        }
        txtCodigo1.setText("");
        txtDescricao.setText("");
            
        
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
           if(TabelaTipoPag.getSelectedRowCount()==0){
            Object[]options = { "OK"};
            JOptionPane.showOptionDialog(null, "Selecione um Típo de Pagamento a ser Alterado ", "Aviso",
            JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE,
            null, options, options[0]);
        }
        else
        {
            jTabProcurar.setSelectedIndex(1);
            int indiceLinha = TabelaTipoPag.getSelectedRow();
            tipoPagamento=daoTpPagamento.consultar(Integer.parseInt((String) TabelaTipoPag.getValueAt(indiceLinha, 0)));
            txtCodigo.setText(String.valueOf(tipoPagamento.getCodigo()));
            txtDescriçao.setText(tipoPagamento.getDescricao());
            jLabelDescricao.setVisible(false);
            if(tipoPagamento.getSituacao().equals("A"))
             rbAtivo.setSelected(true);
            else
              rbInativo.setSelected(true);        
            listar();
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

