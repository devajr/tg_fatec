    private void txtCodigo1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigo1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            tipoPagamento=daoTpPagamento.consultar(Integer.parseInt(txtCodigo1.getText())); 
            DefaultTableModel modelo=montaTabela();
            if(tipoPagamento==null){
                JOptionPane.showMessageDialog(this, "Tipo de Pagamento não encontrado");
            }else{         
                modelo.addRow(new Object[]{String.valueOf(tipoPagamento.getCodigo()),tipoPagamento.getDescricao(),formataSituacao(tipoPagamento.getSituacao())});
            }
            TabelaTipoPag.setModel(modelo);
            TabelaTipoPag.setDefaultEditor(Object.class, null);
            txtCodigo1.setText("");        
        }

    }//GEN-LAST:event_txtCodigo1KeyPressed

